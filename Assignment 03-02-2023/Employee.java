package Collection;

public class Employee {
int empid;
String name;
int salary;
public Employee(int empid, String name, int salary) {
	super();
	this.empid = empid;
	this.name = name;
	this.salary = salary;
}
public int getEmpid() {
	return empid;
}
public String getName() {
	return name;
}
public int getSalary() {
	return salary;
}

}
